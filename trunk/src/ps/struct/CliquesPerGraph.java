package ps.struct;

/**
 * Cliques per graph structure.
 */
public class CliquesPerGraph {

	private CliqueDetails cliqueDetails;
	private GraphDetails graphDetails;

	public CliqueDetails getCliqueDetails() {
		return cliqueDetails;
	}

	public void setCliqueDetails(CliqueDetails cliqueDetails) {
		this.cliqueDetails = cliqueDetails;
	}

	public GraphDetails getGraphDetails() {
		return graphDetails;
	}

	public void setGraphDetails(GraphDetails graphDetails) {
		this.graphDetails = graphDetails;
	}
}
